# 4 example
def get_max(a, b):
    if a > b:
        return a
    else:
        return b


print(get_max(10, 5))
print(get_max(3, 8))


# 5 example
def calculate_area(length, width):
    return length * width


area = calculate_area(5, 10)
print("Area:", area)
